#!/usr/bin/env python3
"""
This module defines a function `concat` that takes two strings as arguments
and returns their concatenation as a single string.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings and returns the result.

    Args:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        str: The concatenated string (str1 + str2).
    """
    return str1 + str2
